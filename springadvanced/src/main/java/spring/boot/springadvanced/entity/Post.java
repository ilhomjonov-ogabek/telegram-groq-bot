import lombok.Setter;
import lombok.ToString;

@Entity
@Getter
@Setter
@ToString
@NoArgsConstructor
@AllArgsConstructor
@Builder
public class Post {

  @Id
  private Integer id;
  private Integer userId;
  private String title;
  private String body;


}
